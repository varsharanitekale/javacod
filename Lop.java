package com.company;

public class Lop {

	public static void main(String[] args) {
		//For Loop 
		for(int i=1; i<=15; i=i+1) {
			System.out.println(i);
		}
		//While Loop
		int j=1;
		while(j<=10) {
			System.out.println(j);
			j=j+1;
		}
		//Do While Loop
		int k=1;
		do {
			System.out.println(k);
			k=k+1;
		}
			while(k<=10);
			
	}
}

		
